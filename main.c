void EEWriteWord (uint16_t speiste, uint32_t var32)
{

	uint8_t speicher1 = 0;
	uint8_t speicher2 = 0;
	uint8_t speicher3 = 0;
	uint8_t speicher4 = 0;
	
	speicher1 = var32;
	speicher2 = var32 >> 8;
	speicher3 = var32 >> 16;
	speicher4 = var32 >> 24;
	
	EEWriteByte(speiste, speicher1);
	speiste++;
	EEWriteByte(speiste, speicher2);
	speiste++;
	EEWriteByte(speiste, speicher3);
	speiste++;
	EEWriteByte(speiste, speicher4);
	
}


uint32_t EEReadWord(uint16_t speiste)
{
	uint32_t var32 = 0;
	
	speiste += 3;
	var32 = EEReadByte(speiste);
	var32 <<= 8;
	
	speiste--;
	var32 += EEReadByte(speiste);
	var32 <<= 8;
	
	speiste--;
	var32 += EEReadByte(speiste);
	var32 <<= 8;
	
	speiste--;
	var32 += EEReadByte(speiste);
	
	return var32;
}