void TWIInit(void)
{
	//set SCL to 400kHz
	TWSR = 0x00;
	TWBR = 0x0C;
	//enable TWI
	TWCR = (1<<TWEN);
}


void TWIStart(void)
{
	//send start signal 
	TWCR = (1<<TWINT)|(1<<TWSTA)|(1<<TWEN);
	while((TWCR & (1<<TWINT)) == 0);
}


void TWIStop(void)
{
	TWCR = (1<<TWINT)|(1<<TWSTO)|(1<<TWEN);
}


void TWIWrite(uint8_t u8data)
{
	TWDR = u8data;
	TWCR = (1<<TWINT)|(1<<TWEN);
	while ((TWCR & (1<<TWINT)) == 0);
}


uint8_t TWIReadACK(void)
{
	//read byte with NACK
	TWCR = (1<<TWINT)|(1<<TWEN)|(1<<TWEA);
	while ((TWCR & (TWINT)) == 0);
		return TWDR;
}


uint8_t TWIReadNACK(void)
{
	//read byte with NACK
	TWCR = (1<<TWINT)|(1<<TWEN);
	while ((TWCR & (1<<TWINT)) == 0);
		return TWDR;
}


uint8_t TWIGetStatus(void)
{
	uint8_t status;
	//mask status
	status = TWSR & 0xF8;
		return status;
}


uint8_t EEWriteByte ( uint16_t u16addr, uint8_t u8data )
{
	uint8_t addr_l, addr_h;
	addr_l = u16addr;
	addr_h = (u16addr>>8);
	
    TWIStart();  
	TWIWrite(EEWrite); 
	TWIWrite(addr_l);
	TWIWrite(addr_h);
	TWIWrite(u8data);
	TWIStop();
	delay_ms(10);
    return SUCCESS;
}


uint8_t EEReadByte ( uint16_t u16addr )
{
	uint8_t addr_l, addr_h;
	addr_l = u16addr;
	addr_h = (u16addr>>8);
	uint8_t u8data = 0;
	
    
    TWIStart();
	TWIWrite(EEWrite);
	TWIWrite(addr_l);
	TWIWrite(addr_h);
    TWIStart();
	TWIWrite(EERead);
	u8data = TWIReadNACK();
    TWIStop();
	delay_ms(10);
	
    return u8data;
}

